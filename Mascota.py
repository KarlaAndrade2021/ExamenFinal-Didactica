class Mascota():
#atributos
    nombre=""
    color=""
    genero=""

#metodos
    def setNombre(self,nombre):
        self.nombre = nombre

    def getNombre(self):
        return self.nombre

    def setColor(self,color):
        self.color = color

    def getColor(self):
        return self.color

    def setGenero(self,genero):
        self.genero = genero

    def getGenero(self):
        return self.genero
